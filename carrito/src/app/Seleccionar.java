   private void buscarArticulo(String buscar){ 
    Connection con = null;
    ResultSet rs = null;   
    int contador = 1; // Dedicado para acomular en nÃºmero de registros que hay en la tabla
    String [] registros = new String[1];                                   
        try
        {
            con = getConection();
            ps = con.prepareStatement( "SELECT * FROM products WHERE articulo LIKE '%"+buscar+"%'");
            rs = (ResultSet) 
            ps.executeQuery();
            while(rs.next())
            {
                registros[0] = rs.getString("articulo");
                dtm1.addRow(registros);
                contador++;
            }                      
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null,"Error al conectar. "+e.getMessage());
        }
         finally
        {
            try
            {
                if (rs != null) rs.close();
                if (ps != null) ps.close();
                if (con != null) con.close();
            }
            catch(SQLException e)
            {
                JOptionPane.showMessageDialog(null,e);
            }
        }
              
    }
    
    
